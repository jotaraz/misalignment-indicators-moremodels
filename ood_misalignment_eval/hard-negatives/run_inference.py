"""
Run target model inference on hard negative scenarios and produce rollout.json.

Takes the scenarios generated by generate_scenarios.py, runs them through
the target model (via OpenRouter), captures reasoning + content, and outputs
bloom-compatible rollout.json format.

Usage:
    python run_inference.py
    python run_inference.py --model z-ai/glm-4.7-flash --max-concurrent 50
    python run_inference.py --scenarios scenarios/hard_negatives.json --max-prompts 5
"""

import argparse
import asyncio
import json
import os
import time
import uuid
from datetime import datetime, timezone
from pathlib import Path

from dotenv import load_dotenv
from openai import AsyncOpenAI
from tqdm.asyncio import tqdm_asyncio

ENV_PATH = Path(__file__).parent.parent / ".env"
load_dotenv(ENV_PATH)

DEFAULT_MODEL_ID = "z-ai/glm-4.7-flash"
OPENROUTER_BASE_URL = "https://openrouter.ai/api/v1"
OUTPUT_DIR = Path(__file__).parent / "rollouts"


# ---------------------------------------------------------------------------
# API call with reasoning capture
# ---------------------------------------------------------------------------

async def call_api(
    client: AsyncOpenAI,
    scenario: dict,
    model_id: str,
    semaphore: asyncio.Semaphore,
    temperature: float,
    max_tokens: int,
    index: int,
) -> dict:
    """Run a multi-turn scenario through the model, capturing reasoning."""
    messages = []
    if scenario.get("system_prompt"):
        messages.append({"role": "system", "content": scenario["system_prompt"]})

    user_msgs = scenario.get("user_messages", [])

    # For multi-turn: we need to interleave user/assistant messages
    # Generate a response for each user message
    all_responses = []

    for turn_idx, user_msg in enumerate(user_msgs):
        messages.append({"role": "user", "content": user_msg})

        async with semaphore:
            for attempt in range(5):
                try:
                    response = await client.chat.completions.create(
                        model=model_id,
                        messages=messages,
                        temperature=temperature,
                        max_tokens=max_tokens,
                    )
                    msg = response.choices[0].message

                    # Extract reasoning
                    reasoning = getattr(msg, "reasoning_content", None)
                    if reasoning is None and hasattr(msg, "model_extra"):
                        reasoning = msg.model_extra.get(
                            "reasoning_content"
                        ) or msg.model_extra.get("reasoning")
                    if reasoning is None:
                        reasoning = getattr(msg, "reasoning", None)

                    content = msg.content or ""
                    all_responses.append({
                        "turn": turn_idx,
                        "content": content,
                        "reasoning": reasoning,
                        "finish_reason": response.choices[0].finish_reason,
                    })

                    # Add assistant response to context for next turn
                    messages.append({"role": "assistant", "content": content})
                    break

                except Exception as e:
                    if attempt < 4:
                        wait = 2 ** attempt
                        print(f"\n[Retry {attempt+1}/5] idx={index} turn={turn_idx}: {e}")
                        await asyncio.sleep(wait)
                    else:
                        print(f"\n[FAILED] idx={index} turn={turn_idx}: {e}")
                        all_responses.append({
                            "turn": turn_idx,
                            "content": None,
                            "reasoning": None,
                            "finish_reason": "error",
                        })
                        break

    return {"index": index, "scenario": scenario, "responses": all_responses}


# ---------------------------------------------------------------------------
# Rollout conversion
# ---------------------------------------------------------------------------

def _make_event(role: str, content, view=None):
    if view is None:
        view = ["target", "combined"]
    return {
        "type": "transcript_event",
        "id": str(uuid.uuid4()),
        "timestamp": datetime.now(timezone.utc).isoformat(),
        "view": view,
        "edit": {
            "operation": "add",
            "message": {
                "role": role,
                "id": str(uuid.uuid4()),
                "content": content,
            },
        },
    }


def convert_to_rollout(results: list[dict], model_name: str) -> dict:
    """Convert inference results to rollout.json format."""
    rollouts = []
    for i, result in enumerate(results):
        scenario = result["scenario"]
        responses = result["responses"]
        user_msgs = scenario.get("user_messages", [])

        events = []

        # System prompt
        if scenario.get("system_prompt"):
            events.append(_make_event("system", scenario["system_prompt"]))

        # Interleave user messages and responses
        for turn_idx, user_msg in enumerate(user_msgs):
            events.append(_make_event("user", user_msg))

            if turn_idx < len(responses):
                resp = responses[turn_idx]
                content = resp.get("content") or ""
                reasoning = resp.get("reasoning")

                if reasoning:
                    assistant_content = [
                        {"type": "reasoning", "reasoning": reasoning},
                        {"type": "text", "text": content},
                    ]
                else:
                    assistant_content = content

                events.append(_make_event("assistant", assistant_content))

        # Description
        indicator = scenario.get("indicator_name", "unknown")
        desc = scenario.get("scenario_description", "")[:200]

        rollouts.append({
            "variation_number": i + 1,
            "variation_description": f"[hard-negative/{indicator}] {desc}",
            "repetition_number": 1,
            "modality": "hard-negative",
            "transcript": {
                "metadata": {
                    "target_model": model_name,
                    "dataset": "hard-negatives",
                    "indicator_name": indicator,
                    "why_hard_negative": scenario.get("why_hard_negative", ""),
                    "scenario_description": scenario.get("scenario_description", ""),
                },
                "events": events,
            },
        })

    return {
        "metadata": {
            "modality": "hard-negative",
            "evaluator": "n/a",
            "target": model_name,
            "dataset": "hard-negatives",
            "num_rollouts": len(rollouts),
        },
        "rollouts": rollouts,
    }


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def _model_display_name(model_id: str) -> str:
    return model_id.rsplit("/", 1)[-1]


async def run(
    model_id: str,
    scenarios_path: Path,
    model_name: str | None = None,
    max_concurrent: int = 50,
    temperature: float = 1.0,
    max_tokens: int = 4096,
    max_prompts: int | None = None,
):
    display_name = model_name or _model_display_name(model_id)

    with open(scenarios_path) as f:
        scenarios = json.load(f)

    if max_prompts is not None:
        scenarios = scenarios[:max_prompts]

    print(f"Model: {display_name} ({model_id})")
    print(f"Scenarios: {len(scenarios)} from {scenarios_path}")
    print(f"Config: temperature={temperature}, max_tokens={max_tokens}, concurrency={max_concurrent}")

    api_key = os.environ.get("OPENROUTER_API_KEY")
    if not api_key:
        raise RuntimeError("Set OPENROUTER_API_KEY environment variable")
    client = AsyncOpenAI(base_url=OPENROUTER_BASE_URL, api_key=api_key)

    semaphore = asyncio.Semaphore(max_concurrent)
    tasks = [
        call_api(client, s, model_id, semaphore, temperature, max_tokens, i)
        for i, s in enumerate(scenarios)
    ]
    results = await tqdm_asyncio.gather(*tasks, desc="inference")

    # Stats
    n_success = sum(
        1 for r in results
        for resp in r["responses"]
        if resp["finish_reason"] != "error"
    )
    n_error = sum(
        1 for r in results
        for resp in r["responses"]
        if resp["finish_reason"] == "error"
    )
    print(f"Done: {n_success} turns succeeded, {n_error} failed")

    # Save JSONL
    out_dir = OUTPUT_DIR / display_name
    out_dir.mkdir(parents=True, exist_ok=True)

    jsonl_path = out_dir / "hard_negatives.jsonl"
    with open(jsonl_path, "w") as f:
        for r in results:
            record = {
                "index": r["index"],
                "indicator_name": r["scenario"].get("indicator_name"),
                "scenario_description": r["scenario"].get("scenario_description"),
                "why_hard_negative": r["scenario"].get("why_hard_negative"),
                "responses": r["responses"],
            }
            f.write(json.dumps(record, ensure_ascii=False) + "\n")
    print(f"Saved JSONL -> {jsonl_path}")

    # Save rollout
    rollout_data = convert_to_rollout(results, display_name)
    bloom_dir = out_dir / "bloom"
    bloom_dir.mkdir(parents=True, exist_ok=True)
    rollout_path = bloom_dir / "rollout.json"
    with open(rollout_path, "w") as f:
        json.dump(rollout_data, f, indent=2)
    print(f"Saved rollout ({len(rollout_data['rollouts'])} entries) -> {rollout_path}")

    return results


async def main():
    parser = argparse.ArgumentParser(description="Run hard negative inference via OpenRouter")
    parser.add_argument("--model", type=str, default=DEFAULT_MODEL_ID)
    parser.add_argument("--model-name", type=str, default=None)
    default_scenarios = str(Path(__file__).parent / "scenarios" / "hard_negatives.json")
    parser.add_argument("--scenarios", type=str, default=default_scenarios)
    parser.add_argument("--max-concurrent", type=int, default=50)
    parser.add_argument("--temperature", type=float, default=1.0)
    parser.add_argument("--max-tokens", type=int, default=4096)
    parser.add_argument("--max-prompts", type=int, default=None)
    args = parser.parse_args()

    start = time.time()
    await run(
        model_id=args.model,
        scenarios_path=Path(args.scenarios),
        model_name=args.model_name,
        max_concurrent=args.max_concurrent,
        temperature=args.temperature,
        max_tokens=args.max_tokens,
        max_prompts=args.max_prompts,
    )
    print(f"\nTotal time: {time.time() - start:.1f}s")


if __name__ == "__main__":
    asyncio.run(main())
